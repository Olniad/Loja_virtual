require_once "conexao.php"; // Certifique-se de que este arquivo está corretamente configurado

// Adicione esta linha para selecionar o banco de dados
$conn->exec("USE kassakismurf;");

if(isset($_POST["btnRegistrar"])){
    $email = $_POST["m-email"];
    $senha = $_POST["m-senha"];

    $checar = $conn->prepare("SELECT * FROM registro WHERE email = :email");
    $checar->bindParam(':email', $email);
    $checar->execute();
    $row_count = $checar->rowCount();

    if($row_count > 0){
        echo "<script>alert('Login já existe!');</script>";
    }
    else{
        $nome = $_POST["m-nome"];
        
        $stmt = $conn->prepare("INSERT INTO registro (email, senha, nome) VALUES (:email, :senha, :nome)");
        $stmt->bindParam(':email', $email);
        $stmt->bindParam(':senha', $senha);
        $stmt->bindParam(':nome', $nome);        
        
        if ($stmt->execute()) {
            echo "<script>alert('Cadastrado com sucesso!');</script>";
        } else {
            echo "<script>alert('Erro ao cadastrar!');</script>";
        }
    }
